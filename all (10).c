#include <stdio.h>
int main(){
    int n;
    int isprime=1;
    printf("Enter the number:\n ");
    scanf("%d",&n);
    for(int i=2;i<n;i++){
        if(n%i==0){
            isprime=0;
        }
    }
    if(isprime==1){
        printf("the number %d is  prime\n",n);
    }
    else{
        printf("the number %d is not prime\n",n);
    }
}